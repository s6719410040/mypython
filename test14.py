# if-elif-else

score = 68

if score >= 80:
    print("A") #colon อย่าลืม
elif score >= 70:
    print("B") #colon อย่าลืม
elif score >= 60:
    print("C") #colon อย่าลืม
elif score >= 50:
    print("D") #colon อย่าลืม
else:  #colon อย่าลืม # *** else สุดท้ายจะเป็น elif ก็ได้
    print("F") #colon อย่าลืม

