# Function คือ การทำงานหนึ่งๆ จะไม่ทำงานหากเราไม่เรียกใช้ (call function)

# Function มีที่มาที่ไปอย่างไรบ้าง
# - Standard Function/Built-in Function มากับภาษาโปรแกรม
# - User-defined Function ฟังก์ชันที่เราสร้างขึ้นเอง
# - Third-party Function ฟังก์ชันที่มีคนอื่นสร้างขึ้นมาให้เราใช้งาน

# Function มี 4 ประเภท
# no parameter no return
# have parameter no return
# no parameter have return
# have parameter have return