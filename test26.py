# สร้างโปรแกรมคำนวณผลคูณของตัวเลข 2 จำนวนที่รับทางแป้นพิมพ์ แล้วแสดงผลทางหน้าจอ ทั้งนี้โปรแกรมจะทำการคำนวณผลคูณจากตัวเลขที่รับทางแป้นพิมพ์ไปเรื่อยๆ จนกว่าผลคูณจะมากกว่า 5000 ถึงจะหยุดการทำงาน

while True:
    n1 = int(input("ป้อนตัวเลขที่ 1: "))
    n2 = int(input("ป้อนตัวเลขที่ 2: "))
    R = n1 * n2
    print(f"ผลคูณของ {n1} และ {n2} คือ {R}")
    if R > 5000:
        print("ผลคูณมากกว่า 5000 โปรแกรมหยุดทำงาน")
        break